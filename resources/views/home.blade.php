    $(document).ready(function () {
        $('#emailForm').on('submit', function (event) {
            event.preventDefault(); // Prevent the default form submission

            // Show loading indicator
            Swal.fire({
                title: 'Sending Email...',
                allowOutsideClick: false,
                onBeforeOpen: () => {
                    Swal.showLoading();
                },
                html: '<div class="spinner-border" style="width: 3rem; height: 3rem;" role="status"><span class="sr-only"></span></div>'
            });

            // Serialize form data
            var formData = $(this).serialize();

            // Send AJAX request
            $.ajax({
                type: 'POST',
                url: '{{ route('send.email') }}', // Replace with your Laravel route
                data: formData,
                success: function (response) {
                    // Hide loading indicator
                    Swal.close();
                    // Display success message
                    Swal.fire({
                        icon: 'success',
                        title: 'Success',
                        text: response.success
                    });

                    // Clear form inputs
                    $('#emailForm')[0].reset();
                },
                error: function (xhr, status, error) {
                    // Hide loading indicator
                    Swal.close();

                    // Display error message
                    Swal.fire({
                        icon: 'error',
                        title: 'Error',
                        text: 'Failed to send email. Please try again later.'
                    });
                }
            });
        });
    });
</script>
